package modelo;

/**
 *
 * @author james
 */
public class Juego {
    
    private String nombreJuego;
    private String autorJuego;
    private String descripcionJuego;
    private String fechaJuego;
    private String linkJuego;
    private String generoJuego;
    private String lenguajeJuego;
   
    
    public String getAutorJuego() {
        return autorJuego;
    }

    public void setAutorJuego(String autorJuego) {
        this.autorJuego = autorJuego;
    }
    public String getNombreJuego() {
        return nombreJuego;
    }

    public void setNombreJuego(String nombreJuego) {
        this.nombreJuego = nombreJuego;
    }

    public String getDescripcionJuego() {
        return descripcionJuego;
    }

    public void setDescripcionJuego(String descripcionJuego) {
        this.descripcionJuego = descripcionJuego;
    }

    public String getFechaJuego() {
        return fechaJuego;
    }

    public void setFechaJuego(String fechaJuego) {
        this.fechaJuego = fechaJuego;
    }

    public String getLinkJuego() {
        return linkJuego;
    }

    public void setLinkJuego(String linkJuego) {
        this.linkJuego = linkJuego;
    }

    public String getGeneroJuego() {
        return generoJuego;
    }

    public void setGeneroJuego(String generoJuego) {
        this.generoJuego = generoJuego;
    }

    public String getLenguajeJuego() {
        return lenguajeJuego;
    }

    public void setLenguajeJuego(String lenguajeJuego) {
        this.lenguajeJuego = lenguajeJuego;
    }
    
    
    
}
